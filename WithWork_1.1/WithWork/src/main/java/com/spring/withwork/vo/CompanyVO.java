package com.spring.withwork.vo;

import java.sql.Date;

public class CompanyVO {
	private int c_num;
	private String name;
	private String g_name;
	private Date regdate;
	private String subject;
	private String thumbnail;
	
	public CompanyVO() {
	}

	public CompanyVO(int c_num, String name, String g_name, Date regdate, String subject, String thumbnail) {
		this.c_num = c_num;
		this.name = name;
		this.g_name = g_name;
		this.regdate = regdate;
		this.subject = subject;
		this.thumbnail = thumbnail;
	}

	public int getC_num() {
		return c_num;
	}

	public void setC_num(int c_num) {
		this.c_num = c_num;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getG_name() {
		return g_name;
	}

	public void setG_name(String g_name) {
		this.g_name = g_name;
	}

	public Date getRegdate() {
		return regdate;
	}

	public void setRegdate(Date regdate) {
		this.regdate = regdate;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}

	public String getThumbnail() {
		return thumbnail;
	}

	public void setThumbnail(String thumbnail) {
		this.thumbnail = thumbnail;
	}

	@Override
	public String toString() {
		return "CompanyVO [c_num=" + c_num + ", name=" + name + ", g_name=" + g_name + ", regdate=" + regdate
				+ ", subject=" + subject + ", thumbnail=" + thumbnail + "]";
	}
	
	
}
